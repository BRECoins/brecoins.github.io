        <h1 class="montserrat-extra">Como faço para cancelar uma transação?</h1>
        <p>Infelizmente não é possível cancelar uma transação que já tenha sido efetivada. Uma característica importante envolvendo transações em bitcoin é a sua irreversibilidade. Isso se dá pela forma como o sistema bitcoin foi desenvolvido, assim como a tecnologia usada para registrar essas transações, a blockchain.</p>
        <p>Assim que são aprovadas todas as transações recebem uma assinatura digital, que nada mais é do que um código criptografado, que informa a todos os conectados ao sistema que a transação é valida. Após receber essa assinatura, a transação é agrupada num bloco na blockchain. Todas essas informações são ligadas umas as outras e criptografadas com um código praticamente impossível de ser quebrado. Mesmo que fosse possível quebrar a criptografia de uma transação, para modifica-la ou cancelá-la, isso implicaria em ter que modificar todas as transações ligadas a ela. Isso seria prontamente repelido pela rede bitcoin. Para saber mais sobra o que é blockchain e como ela funciona, clique aqui.</p>
        <p>Portanto, seguem abaixo algumas medidas importantes podem ajudar para que o usuário realize suas transações com segurança e maior tranquilidade.</p>
        <p><strong>Mantenha sua conta segura</strong></p>
        <p>Ao tomar medidas para manter sua conta segura o usuário evitará possíveis transtornos e perdas que possam resultar devido ao roubo de suas informações. Visto que não é possível cancelar uma transação, os bitcoins perdidos em razão de crimes cibernéticos não poderiam ser recuperados. <a href="/faq/como-posso-deixar-minha-conta-mais-segura.php">Clique aqui</a> e veja algumas medidas que poderão ser tomadas a fim de aumentar a segurança da conta.</p>
        <p><strong>Certifique-se da idoneidade do destinatário</strong></p>
        <p>Toda vez que necessitar realizar uma transação envolvendo moedas digitais, esteja certo quanto a integridade da outra parte envolvida. Caso tenha alguma dúvida quanto a reputação ou idoneidade deste, é melhor não realizar a transação.</p>
        <p>Se o usuário levar em consideração as informações expostas acima, poderá realizar suas transações e usufruir das inúmeras vantagens envolvidas em utilizar moedas digitais.</p>
