        <h1 class="montserrat-extra">Como funciona o Bitcoin?</h1>
        <p>O sistema todo é controlado por um software de código aberto que controla, dentre outras coisas, a quantidade de moeda a ser emitida e o grau de dificuldade para consegui-las.  Todas as transações precisam ser validadas pela rede antes de serem efetivadas, processo esse que recebe o nome de mineração (se você deseja saber mais sobre mineração, clique aqui). Após serem validadas, as transações são incluídas num livro público digital chamado de blockchain (para saber de forma mais ampla o que é blockchain, clique aqui).</p>
        <p>Uma das principais inovações apresentadas no sistema bitcoin foi a descentralização da verificação, validação e manutenção dos dados. Os membros da rede fazem o papel de garantidores de que a transação é fidedigna, impedindo que a mesma moeda seja gasta duas vezes pelo mesmo usuário. Anteriormente, a solução para esse problema era a centralização dos dados onde uma instituição fazia o papel de fiscalização, o que torna as transações mais caras e lentas.</p>
