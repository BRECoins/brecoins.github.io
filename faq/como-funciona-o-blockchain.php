        <h1 class="montserrat-extra">Como funciona blockchain?</h1>
        <p>Após ter uma ideia sobre o que é blockchain, surge outra pergunta: como ela funciona? As transações aprovadas são organizadas em blocos pelo sistema, que juntos formam uma cadeia, daí o nome cadeia de blocos ou blockchain. Um novo bloco é iniciado e fechado num espaço de tempo predeterminado pelo sistema (10 minutos, aproximadamente). Assim que um bloco é fechado gera-se uma assinatura digital que, somada a assinatura do bloco anterior, formam uma assinatura final que será utilizada pelo bloco seguinte. Esse processo se repete indefinidamente fazendo com que todos os blocos fiquem interligados. É como se cada bloco fosse um elo que, unidos, formam uma grande corrente. Todo o processo utiliza um avançado sistema de criptografia, onde as informações são convertidas em um código inquebrável. Mesmo que alguém conseguisse quebrar a criptografia para modificar ou fraudar uma transação, toda a cadeia de blocos seria alterada, movimento esse que seria repelido pela rede.</p>
        <p>Mas, isso não é tudo. Visto que sua base de dados é descentralizada, para que um ciberataque fosse eficaz precisaria invadir ao mesmo tempo todos os computadores que distribuem as informações. Seria como se, imaginando que cada ponto na rede fosse uma página e que ao se unirem formassem um livro, alguém tentasse rasgar todas as páginas de uma só vez. Isso não seria uma tarefa nada fácil, ainda mais se levarmos em consideração que, ainda fazendo uso da metáfora, esse livro contivesse milhares de páginas! Isso sem contar no elevado poder de processamento que seria necessário para quebrar os milhares de códigos criptografados de cada um dos pontos que compõem a rede.</p>
