        <h1 class="montserrat-extra">Como posso deixar minha conta mais segura?</h1>
        <p>Com a crescente utilização de meios eletrônicos como métodos de pagamento, houve um aumento considerável de crimes cibernéticos, ou cibercrimes. Estima-se que só no ano de 2016 aproximadamente 689 milhões de pessoas em 21 países diferentes sofreram com alguma forma de cibercrime (segundo o relatório Norton Cyber Security Insights). Embora o sistema que gerencia as transações em criptomoedas usufrua de métodos avançados no quesito segurança (saiba mais clicando aqui), no que se refere a informações pessoais, tais como senhas e proteção de dispositivos eletrônicos, são de inteira responsabilidade do usuário. Seria como ter itens valiosos guardados dentro de um cofre, porém, guardar de forma segura a senha para abri-lo é de responsabilidade do proprietário dos itens. Como essa porta pode ser fechada e assim diminuir o risco de ter informações pessoais furtadas?</p>
        <p><strong>Não utilize senhas óbvias</strong></p>
        <p>Procure combinar números, letras (maiúsculas e minúsculas) e caracteres especiais. Quanto mais complexa for a senha, mais difícil será para que alguém possa quebrá-la. Nunca utilize senhas muito óbvias tais como datas, números de documentos ou combinações excessivamente simples, como por exemplo "0101" ou "1234".</p>
        <p><strong>Não compartilhe suas senhas</strong></p>
        <p>Nunca compartilhe suas senhas com quem quer que seja, em especial por meios eletrônicos. Procure memorizá-las ao invés de guardá-las por escrito. Caso seja necessário ter cópias físicas, guardá-las em local seguro.</p>
        <p><strong>Mude as senhas periodicamente</strong></p>
        <p>Procure mudar as senhas periodicamente, evitando repeti-las. Evite utilizar a mesma senha em mais de um site ou plataforma de acesso.</p>
        <p><strong>Ter um bom antivírus e firewall instalados</strong></p>
        <p>Ter instalado no dispositivo eletrônico um bom software antivírus e firewall, e mantenha-os ativados e atualizados. Além disso nunca visite sites suspeitos, visto que podem conter conteúdos maliciosos. Na dúvida, não prossiga.</p>
        <p><strong>Utilizar autenticação em 2 passos</strong></p>
        <p>Sempre que disponível ativar a opção de autenticação de 2 passos. Essa medida aumenta a segurança da conta visto que necessita de confirmação para acessá-la.</p>
        <p><strong>Cuidado com a navegação</strong></p>
        <p>Nunca clicar em links desconhecidos ou de origem duvidosa. O mesmo vale para links enviados por e-mail.</p>
