        <h1 class="montserrat-extra">Como realizar uma transferência entre carteiras?</h1>
        <p>Transferir bitcoin de uma carteira para outra é uma tarefa rápida e simples. Confira:</p>
        <p><strong>Enviar Bitcoin</strong></p>
        <p>Para enviar bitcoin para uma outra carteira, o usuário deverá realizar o procedimento abaixo:</p>
        <ol>
          <li>
            <p>Em primeiro lugar o usuário deverá possuir o endereço da carteira de quem receberá o bitcoin.</p>
          </li>
          <li>
            <p>Em seguida deverá acessar a sua própria carteira.</p>
          </li>
          <li>
            <p>Após o procedimento anterior selecione a opção "SEND" ou "ENVIAR".</p>
          </li>
          <li>
            <p>Digite ou cole em seus respectivos espaços o endereço da carteira de destino e a quantidade de bitcoin a ser enviada. Confirme a transação.</p>
          </li>
        </ol>
        <p><strong>Receber Bitcoin</strong></p>
        <p>Para receber bitcoin de outra carteira, o usuário deverá realizar o procedimento abaixo:</p>
        <ol>
          <li>
            <p>Selecione a opção "RECEIVE" ou "RECEBER".</p>
          </li>
          <li>
            <p>Será gerado um código composto por letras e números. Copie esse código.</p>
          </li>
          <li>
            <p>Envie o endereço gerado para o usuário que enviará o bitcoin.</p>
          </li>
        </ol>
        <p>Todo o processo poderá ser realizado através de códigos QR. Para tanto basta possuir um dispositivo com uma câmera e escanear o código gerado pela carteira. O código QR funcionará como substituto do endereço a ser digitado pelo usuário.</p>
        <p>Algumas carteiras oferecem outras funcionalidades além de apenas enviar ou receber o bitcoin, como por exemplo a opção de editar e assim aumentar ou diminuir a taxa de mineração (este procedimento influi diretamente na velocidade de transferência visto que, quanto maior a taxa de mineração, maior a velocidade com que a transação será efetivada), compatibilidade com outras moedas digitais e até troca de moedas. Saiba mais sobre carteira de bitcoin clicando aqui.</p>
        <p>Lembramos que o passo a passo descrito acima trata-se de funções básicas de uma carteira e que embora possa ser ligeiramente diferente em virtude da carteira escolhida, o processo será basicamente o mesmo. Portanto o usuário poderá utilizar as informações descritas nesse tópico para nortear sua utilização independentemente da carteira.</p>
