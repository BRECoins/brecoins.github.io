        <h1 class="montserrat-extra">O que é autenticação de dois fatores?</h1>
        <p>A <strong>autenticação de dois fatores</strong> é um processo onde o acesso para uma determinada conta passa a ser feito divido em duas camadas ou etapas. Esse tipo de autenticação vem sendo amplamente utilizada visto que oferece mais segurança para o usuário. Assim, muitos serviços, como por exemplo provedores de contas de e-mails, têm disponibilizado essa ferramenta para seus usuários.</p>
        <p><strong>Como funciona a autenticação de dois fatores?</strong></p>
        <p>A primeira camada é acessada por meio de uma senha pré-cadastrada pelo usuário e funciona como as senhas de acessos comumente utilizadas para acessar contas na web, como por exemplo contas de e-mails. É importante que o usuário nunca utilize senha básicas ou a mesma senha em mais de uma conta. Caso deseje saber mais sobre como deixar sua conta mais segura, clique aqui. Na segunda camada é solicitado para o usuário uma confirmação geralmente por meio de um token gerado por um aplicativo no celular ou enviado para o e-mail previamente cadastrados.</p>
        <p>Esse método tem se mostrado eficaz como medida para melhorar a segurança no acesso a uma conta. Isso porque para que alguém mau intencionado conseguisse acessar a conta de alguém precisaria primeiro roubar a senha e, além disso, ter acesso ao celular ou ao e-mail da vítima.</p>
        <p><strong>Quando devo ativar a autenticação de dois fatores?</strong></p>
        <p>Sempre que um serviço disponibilizar essa ferramenta e contenha informações importantes seria ideal que a autenticação de dois fatores seja ativada. Embora esse não seja um método infalível, pode ser encarado como mais uma boa ferramenta de segurança a disposição do usuário.</p>
        <p>Pensando na segurança de seus clientes, a BRE Coins torna obrigatório o uso da autenticação de dois fatores desde o cadastro. Os tokens são enviados por e-mail. No entanto, é recomendado sincronizar um aplicativo gerador de códigos pelo celular, para garantir mais segurança e praticidade.</p>
