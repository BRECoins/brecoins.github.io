        <h1 class="montserrat-extra">O que é Blockchain?</h1>
        <p>Podemos definir o que é blockchain como sendo uma tecnologia onde os dados são estruturados em blocos, sendo que a validação dos dados ocorre de forma descentralizada. Ela funciona como um livro público digital e compartilhado onde operações são validadas e registradas num ambiente virtual. Essa ideia teve sua primeira menção em 2008, com a publicação de um artigo creditado a Satoshi Nakamoto. Desde então, muitos estudos estão em andamento para que a essa tecnologia possa ser adaptada e empregada às mais variadas formas de utilização além de moedas digitais, como por exemplo, contratos inteligentes, registro de imóveis, títulos de crédito, consórcios, prontuários médicos, dentre outras.</p>
