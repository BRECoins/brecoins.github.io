        <h1 class="montserrat-extra">O que é FORK?</h1>
        <p>Os forks são atualizações realizadas no protocolo ou código de uma criptomoeda. Um fork normalmente acontece com o objetivo de trazer algum aprimoramento para o código de uma moeda digital. Eles podem ocorrer de duas formas diferentes: Soft Fork ou Hard Fork. O que são e qual é a principal diferença entre eles?</p>

<p><strong>Soft Fork</strong></p>

<p>Quando a comunidade de desenvolvedores e mineradores chega a um consenso com relação a uma atualização ela é lançada e implementada, o que é chamado de soft fork. Na grande maioria das vezes essas atualizações não são sentidas pelos usuários comuns, ou seja, aqueles que utilizam a moeda digital apenas para realizar suas transações. Essas atualizações podem ocorrer por inúmeros motivos, como por exemplo melhorias de segurança, falhas identificadas, dentre outras.</p>

<p><strong>Hard Fork</strong></p>

<p>Em algumas vezes a maioria dos membros da comunidade acaba optando por não lançar uma atualização por acreditarem que deveriam desenvolve-la melhor ou por qualquer outro motivo. No entanto, em alguns casos um grupo continua acreditando que a atualização é necessária ou vantajosa. Quando isso acontece, o grupo divergente talvez decida lançar a atualização mesmo sem o consentimento da comunidade, criando uma nova moeda digital. Esse procedimento recebe o nome de hard fork. O Bitcoin Cash e o Bitcoin Gold podem ser citados como exemplos de hard forks.</p>

<p><strong>Como o fork impacta o usuário comum?</strong></p>

<p>Para a maioria dos usuários de moedas digitais o fork não resulta em nenhuma mudança significativa. Porém, quando um hard fork acontece e a blockchain passa a ser dividida em duas (uma para a moeda atual e outra para a nova moeda), o número de moedas digitais que o usuário possui é copiado para a nova blockchain da nova moeda. Isso aconteceu nos exemplos já mencionados acima, Bitcoin Cash e Bitcoin Gold, ou seja, os usuários que possuíam Bitcoin e mantinham consigo o controle de suas chaves privadas receberam a mesma quantidade da nova moeda. Saiba mais sobre chaves públicas e privadas clicando aqui.</p>

<p>É importante notar que qualquer usuário avançado é capaz de realizar um hard fork a qualquer momento. Se a segurança não for levada a sério, uma moeda fruto de um hard fork pode representar riscos para os fundos em bitcoin de um usuário. Além disso, não há garantias de que haverá aceitação de uma nova moeda por parte do mercado ou das exchanges. Portanto, se o usuário possui interesse em obter uma moeda após um hard-fork, deve-se, anteriormente, sacar os fundos para uma carteira sob seu controle.</p>

<p><strong>O que é um ataque de replay?</strong></p>

<p>Após um hardfork e subsequente divisão da blockchain, seus novos fundos passam a estar disponíveis na blockchain da nova moeda. Entretanto, se a nova moeda não for protegida contra ataques de replay, todos os códigos lançados por seu software de carteira à rede blockchain da nova moeda poderá ser repetido na rede bitcoin, transferindo seus fundos em seu nome. Por este motivo, antes de adotar uma nova moeda fruto de hard-fork, certifique-se de que esta possua proteção contra ataques de replay.</p>


