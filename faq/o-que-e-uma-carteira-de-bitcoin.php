      <h1 class="montserrat-extra">O que é uma carteira de bitcoin (wallet)?</h1>
      <p>Carteira ou wallet é um local onde o usuário armazena as chaves privadas que dão acesso as suas moedas digitais. Elas são classificadas como hot (quente), carteiras on-line necessitando assim de internet para seu funcionamento, ou cold (fria), carteiras que funcionam off-line.</p>
      <p>Dentre os dois grupos já mencionados, segue abaixo uma breve descrição de alguns tipos de carteiras disponíveis.</p>
      <p><strong>HOT</strong></p>
      <p><strong>Web Wallet</strong><strong>.</strong> Esse tipo de carteira exige uma conexão constante com a rede. As chaves privadas relacionadas as moedas digitais de posse do usuário ficam disponíveis num ambiente on-line para serem utilizadas a qualquer momento.</p>
      <p>A principal vantagem desse tipo de carteira é a praticidade de poder transacionar as moedas disponíveis nela a qualquer momento, de forma instantânea. Como desvantagem pode ser destacado o risco relacionado a segurança, visto que ambientes web são mais susceptíveis a ataques cibernéticos.  Além disso, carteiras on-line não oferecem a garantia de que suas chaves privadas não possam ser lidas por terceiros.</p>
      <p><strong>App Wallet</strong><strong>.</strong> O funcionamento do app wallet é similar ao da carteira on-line, porém voltado a dispositivos móveis tais como smartphones e tablets. Funcionam na forma de aplicativo onde o usuário pode tanto enviar como receber moedas digitais.</p>
      <p>Sua vantagem em relação as outras modalidades de carteira é a mobilidade pois podem ser realizadas em qualquer lugar. Uma desvantagem está relacionada ao fato das chaves privadas estarem armazenadas num dispositivo móvel, que pode ser perdido, roubado ou até mesmo invadido. Alguns aplicativos recorrem, ainda, ao armazenamento de chaves privadas em um servidor na internet, o que, de forma semelhante às web wallets, não dá a garantia de que as chaves privadas permaneçam sem acesso de terceiros.</p>
      <p><strong>Desktop Wallet</strong><strong>.</strong> São carteiras para serem utilizadas no desktop que funcionam como um software. Existem várias opções com diferentes funcionalidades.</p>
      <p>Têm como vantagem a praticidade de ter facilmente disponível as chaves para transacionar as moedas digitais. A desvantagem é que este tipo de carteira não se mostra tão segura quanto uma opção de hardware wallet ou outra opção off-line. Além disso, as carteiras cuja implementação corresponde às especificações oficiais do bitcoin requerem o download de toda a blockchain, armazenando o histórico completo desde a primeira transação ocorrida pelo bitcoin. Estes dados tendem a aumentar em tamanho, e já representam mais de 100 GB de dados, atualmente.</p>
      <p><strong>COLD</strong></p>
      <p><strong>Hardware Wallet</strong><strong>.</strong> São dispositivos físicos onde as chaves privadas podem ser armazenadas. Normalmente são utilizados para guardar volumes maiores de moedas digitais visto que são considerados mais seguros por não estarem conectados a rede.</p>
      <p>O mercado já possui vários modelos de hardwares wallets variando em tamanho, funcionalidades e tecnologia.</p>
      <p><strong>Paper Wallet</strong><strong>.</strong> Este tipo de carteira de bitcoin funciona como um documento impresso em papel contendo a(s) chave(s) privada(s) da carteira onde se armazena a moeda digital. Este documento deverá ser utilizado quando o usuário desejar realizar uma transação, já que a chave privada é necessária para fazer uso dos fundos. Este tipo de carteira tem o mesmo conceito do hardware wallet, ou seja, armazenar as chaves privadas de forma off-line.</p>
      <p>Neste modelo de carteira possui como vantagem a segurança, visto que as chaves privadas não ficam armazenadas em ambiente virtual, ficando sob total controle do usuário. Um aspecto negativo é que, por se tratarem de informações impressas em papel, correm o risco de serem perdidas, furtadas ou danificadas. Cabe ao usuário tomar todas as precauções com relação a preservação do documento pois, caso este seja perdido ou inutilizado, não há como ter acesso às chaves privadas necessárias para transacionar as moedas digitais.</p>
