        <h1 class="montserrat-extra">O que são Altcoins?</h1>
        <p>Altcoins são moedas digitais que surgiram após o lançamento do Bitcoin e em sua maioria seguem os mesmos conceitos. Com a popularidade do bitcoin, seu conceito tornou-se referencia e inspiração para que desenvolvedores criassem diversas outras moedas digitais.</p>
        <p>Elas podem variar quanto a funcionalidades e objetivos. Enquanto umas possuem objetivos parecidos com o bitcoin, apresentando apenas supostas melhorias, outras procuram expandir o universo das moedas digitais para além do uso como meio de pagamento ou transferência de valores (como é o caso do Ethereum, por exemplo). Ainda outras destacam-se por características específicas, como por exemplo segurança ou maior privacidade (um exemplo disso é o DASH).</p>
        <p>De acordo com o site coinmarketcap.com existem oficialmente mais de 1200 altcoins no mercado. Mas como surge uma altcoin? Uma altcoin pode surgir basicamente de duas formas: através de um Fork ou de uma ICO.</p>
        <p><strong>FORK</strong></p>
        <p>Quando a comunidade de mineradores e desenvolvedores responsáveis pelo sistema bitcoin não chega a um consenso sobre uma modificação, atualização ou funcionalidade, podem decidir dividir-se, o que é conhecido como hard fork. Essa divisão acaba dando origem a uma nova moeda, como por exemplo no caso do Bitcoin Cash e do Bitcoin Gold. Clique aqui e saiba mais sobre fork.</p>
        <p><strong>ICO (Initial Public Offering)</strong></p>
        <p>Diferentemente das moedas originadas de um Fork, as ICOs se originam de projetos e são financiadas pela captação de investidores, que recebem como contrapartida um lote pré-acordado da nova moeda assim que esta estiver disponível no mercado. Um exemplo de sucesso desse tipo de moeda é o Ethereum. Caso deseje saber mais sobre ICO clique aqui.</p>
        <p><strong>Cuidados necessários</strong></p>
        <p>Com a constante expansão das moedas digitais, alguns cuidados são necessários a fim de evitar a perda de recursos ou até mesmo ser vítima de fraudes. Assim, antes de investir na compra de uma altcoin é preciso certificar-se da idoneidade do projeto ou do vendedor da suposta moeda digital. Algumas perguntas podem ajudar a tomar a decisão correta, como: Caso se trate de uma ICO, o projeto está disponível para pronta pesquisa e avaliação? O projeto foi amplamente divulgado, ou é desconhecido até mesmo das comunidades de moedas digitais? Em caso de compra de moedas já existentes, conheço o vendedor ou sua reputação? Busco mais informações e notícias relacionadas a moeda alvo?</p>
