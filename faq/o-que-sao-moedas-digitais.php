        <h1 class="montserrat-extra">O que são moedas digitais?</h1>
        <p>Moedas digitais, ou criptomoedas, são tokens (códigos) digitais que podem funcionar como meios de pagamento ou transferência de recursos por meio eletrônico funcionando como dinheiro, porém inexistente fisicamente. Em sua maioria possuem como característica principal a desvinculação com entidades governamentais ou órgãos reguladores.</p>
        <p>Atualmente existem inúmeros estabelecimentos comerciais no mundo todo que já aceitam moedas digitais como meio de pagamento. Esse número tem aumentado consideravelmente com o aumento da utilização de moedas digitais no cotidiano.</p>
        <p><strong>Riscos</strong></p>
        <p>No âmbito financeiro, risco está relacionado a probabilidade de que alguma variável, ou mais de uma, possa impactar negativamente uma operação. No que se relaciona às moedas digitais, existem algumas variáveis que devem ser levadas em conta para que a possibilidade de eventuais perdas seja mitigada ao máximo.</p>
        <p>Um ponto de atenção diz respeito à volatilidade das moedas digitais. De forma simplificada, volatilidade pode ser definida como a possibilidade de variação tanto positiva quanto negativa que um ativo possui. Historicamente as moedas digitais demonstraram ter um elevado índice de volatilidade, o que pode resultar em perdas ou ganhos consideráveis para o usuário. Isso se dá basicamente devido ao fato de que as criptomoedas não possuem um órgão controlador que interfira no seu preço, estando sujeitas apenas as leis de mercado (oferta e demanda). Devido a essa característica, o preço das criptomoedas são mais suscetíveis a oscilar diante de especulação de mercado, notícias, ações políticas, dentre outras, do que moedas físicas estatais. Como exemplo, em janeiro de 2017 o valor de mercado do bitcoin era de US$972,95, já em novembro do mesmo ano a moeda bateu seu recorde histórico, alcançando a casa dos US$ 8166,77 (fonte: <a href="https://coinmarketcap.com/currencies/bitcoin/#charts">https://coinmarketcap.com/currencies/bitcoin/#charts)</a>). Portanto, aqueles que desejam utilizá-las como meio de investimento precisam estar sempre atentos aos fatores que possam impactá-las a fim de diminuir a possibilidade de perdas expressivas.</p>
        <p>Outro ponto importante é ter a prudência de não investir aquilo que não está disposto ou não pode perder. Em vista do que foi exposto no parágrafo anterior, o trader precisa ter consciência de que operar com criptomoedas é uma atividade de elevado nível de risco. Portanto, seria prudente diversificar os investimentos além de investir apenas recursos que, se perdidos, não causassem um dano irreparável e destrutivo para o investidor.</p>
        <p>O terceiro aspecto importante a ser avaliado refere-se à irreversibilidade das transações. Uma vez que uma transação é efetivada não é possível estornar ou solicitar reembolso dos fundos, exceto se o próprio destinatário decidir fazer isso. Portanto, quando o usuário deseja realizar uma transação, é essencial que esteja certo da idoneidade do destinatário a fim de evitar maiores transtornos.</p>
        <p>Assim como quando se adquire qualquer forma de ativo ou investimento, é preciso estar ciente tanto dos benefícios como também dos riscos envolvidos na aquisição e uso de moedas digitais. Portanto, é imprescindível a busca por conhecimento para evitar que variáveis não consideradas produziam um resultado aquém da expectativa.  Desta forma o trader pode maximizar seus resultados e otimizar suas operações.</p>
