        <h1 class="montserrat-extra">O que são os mineradores?</h1>
<p>Se alguém já ouviu falar sobre bitcoins provavelmente já ouviu, ou ouvirá, falar sobre minerar bitcoin. Mas o que são os mineradores?</p>

<p>No sistema bitcoin, os dados são organizados na blockchain de forma descentralizada, portanto, é necessário que uma rede global valide as transações e garantam a legitimidade destas. Os membros dessa rede recebem o nome de "mineradores". Essa forma de processamento difere dos sistemas convencionais, como os de bancos por exemplo, onde o garantidor da segurança da transação e dos dados é um único indivíduo ou organização, no caso o próprio banco. Já no caso da blockchain, os membros da rede são os garantidores da segurança e manutenção dos dados. É como se cada membro possuísse uma cópia desse livro público e que para alterar alguma coisa nele fosse preciso ter a autorização de todos os membros e realizá-la de forma simultânea em todos os computadores ligados a rede.</p>

<p>Mas como funcionada o trabalho dos mineradores e o quê eles recebem para desenvolvê-lo? A cada 10 minutos o sistema bitcoin envia um código envolvendo fórmulas matemáticas para a rede soluciona-las. Assim que um dos membros consegue resolver o problema é criado um bloco onde as transações realizadas pelos usuários e validadas pela rede são organizadas. Como recompensa o minerador recebe uma quantia de bitcoins, quantia essa determinada pelo software. Além disso, uma pequena taxa é cobrada do usuário quando realiza uma transferência de bitcoins que é destinada ao minerador como "pagamento" pelo seu trabalho de validação.</p>

<p><strong>O que é preciso para minerar bitcoin?</strong></p>

<p>Para poder minerar é necessário baixar e instalar a carteira oficial do bitcoin, bem como os dados da blockchain. Essa tarefa pode durar um tempo considerável visto que ao baixar os dados da blockchain o usuário receberá todas as transações realizadas em bitcoins até o momento da instalação do software.</p>

<p>Além disso, o usuário precisa de um computador com altíssimo poder de processamento. Atualmente existem empresas que fabricam máquinas especificas para mineração. Quando o sistema bitcoin foi lançado qualquer computador poderia ser utilizado para minerar bitcoin, desde que ficasse o tempo todo ligado. Porém, o software foi desenvolvido para que a dificuldade seja aumentada periodicamente à medida que mais moedas forem distribuídas.</p>

