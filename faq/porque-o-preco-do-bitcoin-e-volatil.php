        <h1 class="montserrat-extra">Por que o preço do bitcoin é volátil?</h1>
        <p>De forma simplificada, volatilidade pode ser definida como a possibilidade de variação tanto positiva quanto negativa que um ativo possui. Historicamente as moedas digitais demonstraram ter um elevado índice de volatilidade, o que pode resultar em perdas ou ganhos consideráveis para o usuário. Isso se dá basicamente devido ao fato de que as criptomoedas não possuem um órgão controlador que possa interferir em sua cotação ou preço, assim como acontece com as moedas fiduciárias. Sendo assim, o preço do bitcoin fica sujeito apenas às leis de mercado (oferta e demanda). Dessa forma, quanto mais usuários desejarem comprar, o preço tende a subir visto que a demanda se tornará maior que a oferta. Similarmente, quanto mais pessoas desejarem vender o preço tenderá a cair visto que a oferta se torna maior do que a demanda.</p>
        <p>Devido a essa característica, o preço das criptomoedas são mais suscetíveis a oscilar diante de especulação de mercado, notícias, ações políticas, dentre outras, do que moedas físicas estatais. Portanto, aqueles que desejam utilizá-las como meio de investimento precisam estar sempre atentos aos fatores que possam impactá-las a fim de diminuir a possibilidade de perdas expressivas.</p>
        <p>No entanto, com base nos dados históricos, é possível observar uma grande e constante valorização no preço do bitcoin. Isso tem ocorrido especialmente devido a consolidação das moedas digitais como meio de transferência de recursos. Muitas companhias, incluindo alguns gigantes como a DELL, por exemplo, já aceitam o bitcoin como forma de pagamento para produtos e serviços.</p>
        <p><strong>Votalitidade do Bitcoin</strong></p>
        <p>Um aspecto a ser levado em consideração quando se analisa a <strong>volatilidade do bitcoin</strong> é que seus efeitos são mais impactantes em investidores de curto prazo. Isso porque ao realizar uma análise histórica do preço da moeda é possível perceber que as maiores oscilações e movimentos de queda não afetaram a moeda a longo prazo. Prova disso é que apenas no ano de 2017, levando-se em conta os meses de Janeiro a Novembro, a moeda apresentou uma valorização de 650%, de acordo com a revista Época Negócios ( <a href="http://epocanegocios.globo.com/Mercado/noticia/2017/11/epoca-negocios-bitcoin-atinge-marca-de-us-7000-pela-primeira-vez.html">http://epocanegocios.globo.com/Mercado/noticia/2017/11/epoca-negocios-bitcoin-atinge-marca-de-us-7000-pela-primeira-vez.html)</a>).</p>
