        <h1 class="montserrat-extra">Quais são os tipos de ordens disponíveis na BRE COINS?</h1>
        <p>A BRE Coins oferece os mais avançados tipos de ordens para que o trader possa maximizar seu desempenho e, por consequência, seus resultados. O usuário poderá escolher o tipo mais apropriado de acordo com suas necessidades. Vale lembrar que a disponibilidade de alguns tipos de ordens para venda ou <strong>compra de bitcoin</strong> estarão sujeitas a categoria de conta que o usuário pertence. Para saber mais a respeito clique aqui.</p>
        <p><strong>ORDEM ATIVA E PASSIVA</strong></p>
        <p><strong>Ordem de Mercado ou Ativa</strong></p>
        <p>Essa ordem é muito utilizada por causa da agilidade e rápida liquidez. Nela o usuário define a quantidade de moeda que deseja comprar ou vender. O preço é definido de acordo com o preço médio de mercado no momento da execução da ordem.</p>
        <p><strong>Ordem Limite ou Passiva</strong></p>
        <p>Mais conhecida como ordem passiva, neste tipo de ordem o usuário limita a um determinado valor para que a ordem seja executada. Ela permanecerá aberta no livro de ordens até uma um comprador aceite o valor predefinido pelo vendedor.</p>
        <p><strong>OUTRAS ORDENS</strong></p>
        <p><strong>Ordem Stop</strong></p>
        <p>Nesta modalidade de ordem de venda ou <strong>compra de bitcoin</strong> se torna ativa apenas quando o valor predeterminado pelo usuário é alcançado. No caso das ordens de venda, podem ser utilizadas para proteger o trader contra grades perdas quando a moeda entrar num viés de baixa, visto que um piso é definido para que a ordem seja enviada automaticamente. Um ponto importante a ser lembrado é que a ordem stop será enviada ao livro de ofertas quando o valor de mercado atingir o valor stop, porém ela será executada pelo melhor valor abaixo do valor predefinido.</p>
        <p><strong>ORDENS EM ELABORAÇÃO</strong></p>
        <p><strong>Ordem FOK (Fill Or Kill)</strong></p>
        <p>Neste tipo de ordem o usuário instrui o sistema para que compre ou venda imediatamente um valor determinado de ativos a um preço predefinido. Caso os parâmetros instruídos não sejam satisfeitos a ordem deixará de existir automaticamente.</p>
        <p><strong>Ordem Iceberg</strong></p>
        <p>Na ordem iceberg o usuário pode comprar ou vender uma grande quantidade de moedas digitais, porém instruir o sistema para que apenas uma quantidade específica fique aparente no livro de ofertas. Seria como se uma grande ordem fosse particionada em várias pequenas ordens que são incluídas no livro a medida em que as anteriores são executadas. Ela é especialmente útil quando o trader deseja comprar ou vendar uma grande quantidade de ativos sem que isso influencie o mercado e, por consequência, impacte negativamente no resultado.</p>
        <p><strong>Ordem OCO (One Cancels Other)</strong></p>
        <p>Mais de uma ordem pode ser lançada com diferentes parâmetros. Caso uma delas seja executada as demais são canceladas. Esse tipo de ordem é muito útil quando o usuário deseja estabelecer mais de um parâmetro de entrada ou saída (compra ou venda).</p>
        <p><strong>Ordem TWAP (Time-Weighted Average Price)</strong></p>
        <p>Neste modelo, uma grande ordem será dividida em várias ordens menores que serão enviadas para o livro distribuídas num período predeterminado pelo trader. Isso evita que uma ordem com um volume consideravelmente alto cause um impacto negativo no mercado.</p>
